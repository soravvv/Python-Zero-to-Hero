a = "1"
b = "2"
print(int(a) + int(b)) # explicit
print(a + b) # implicit 

# Explicit Conversion- The conversion of one data type into another data type, done via developer as per the requirement is known as explicit type conversion

# Implicit Conversion- The conversion of one data type is converted into other by the python interpreter itself(automatically). This is called, implicit data type.